        <div id="web">
            <div id="subtitle">Web Resources</div>
            <div id="web-links">
                <ul>
                <li><a href="https://www.youtube.com/watch?v=zGBa2IVgoOo">Metal Casting: Medieval Pilgrim Badge</a></li>
                </ul>
                <p>This video was produced by the Digital Pilgrim Project and demonstrates the most common method of producing medieval badges. Not only is it useful for understanding how these badges were produced, but it also aids in understanding exactly how they ended up being so widely distributed by demonstrating their quick production time.</p>
                <ul>
                <li><a href="https://kunera.nl/en">Kunera Database</a></li>
                </ul>
                <p>The Kunera database is a continuous project underway at Radboud University to collect, document, and digitize medieval badges. Currently, their online database has records of 26477 badges and includes an interactive map which plots both pilgrimage sites and find locations of the badges in their collection.</p>
                <ul>
                <li><a href="https://www.medievalbadges.ca/">Medieval Badges</a></li>
                </ul>
                <p>This is the site for a recent SSHRC-funded project dedicated to the research of medieval badges, examining the function of them in everyday life. It features informative blog posts by the researchers involved in the project, as well as a summary of select current resources related to formal research and experiential learning with medieval badges.</p>
                <ul>
                <li><a href="https://sketchfab.com/britishmuseum/collections/digital-pilgrim">The Digital Pilgrim Collection</a></li>
                </ul>
                <p>This is the entirety of the <em>Digital Pilgrim</em> badge collection on Sketchfab, which includes more badges related to St Thomas Becket, as well as a handful of secular badges. Through this collection, one can also access the British Museum&#39;s Sketchfab account as a whole and view the other 3D models that have been created from objects in their archive.</p>
                <ul>
                <li><a href="https://gallica.bnf.fr/ark:/12148/btv1b6000784s/f7.double">Vie et Miracles de Monseigneur Saint Louis (Livre des Faits de Monseigneur Saint Louis)</a></li>
                </ul>
                <p>This is the entirety of the book which is discussed in the final section of this exhibition; it is an illuminated account of the life of King Louis IX, the only French king to be canonized, commissioned by Charles II of Bourbon, a former Archibishop of Lyon. Although the text may be unreadable for those who do not know French, there are numerous artistic renditions of badges featured in the illustrations which offer a point of reference regarding the place badges held in the daily life of medieval Catholic culture.</p> 
