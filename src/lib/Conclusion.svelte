    <div id="title">Conclusions</div>
    <div id="text">
        <p>This exhibition only touches the surface of how medieval badges express the visual culture and communication which developed in the Middle Ages, yet in the observation of just three badges, there is still a wealth of information uncovered from these unassuming and deteriorating objects. The reuse of common symbols are combined in a variety of different ways to create a context which makes the origin and meaning of each badge clear. Through analysing the 3D representations of these objects which otherwise cannot be handled, an authentic tactile, archival experience is replicated which allows for in-depth analysis of the badges at both the macroscopic and microscopic level. Angles and details that could only be expressed through numerous pictures are instead made accessible through a singular, complete form. And, in understanding the form of these once common objects, we gain a greater ability to spot them in other circumstances, gaining enhanced context about not only the symbolism of these objects but how they were presented and used.</p>
        <p>The following is a list of both web-based and traditional resources that can be used to learn more about these fascinating small badges that were so present in medieval life.</p>
    </div>

