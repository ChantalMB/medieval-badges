<p>This is an archival entry for the badge depicting St John the Baptist, produced for Amiens Catherdral. The descriptive notes of the entry were referenced for the translation of latin inscribed on the border of this badge.</p>
<ul>
<li>Mellinkoff, Ruth. <em>Averting Demons: The Protective Power of Medieval Themes and Motifs</em>. Los Angeles: Ruth Mellinkoff Publications, 2004.</li>
</ul>
<p>In this study of religion and art, Mellinkoff highlights the beliefs surrounding good and evil as understood in medieval Catholic tradition and relates this to the usage of symbols within the forms of art produced in this time period. It is a point of reference for both Catholic belief in relation to demonic entities, and also the meaning and use of various motifs in medieval works such as badges.</p>
<ul>
<li>Rasmussen, Ann Marie. <em>Medieval Badges: Their Wearers and Their Worlds</em>. Philadelphia: University of Pennsylvania Press, 2021.</li>
</ul>
<p>This book is the most recently published, in-depth overview of medieval badges. It provides insight into badges in both religious and secular contexts, highlighting the points of intersection between these commonly divided categories, expanding upon the different ways badges may confer meaning and identity on their wearers.</p>
