<li>Bede. <em>Bede&#39;s Ecclesiastical History of England</em>. Translated by A. M. Sellar. London: George Bell and Sons, 1907.</li>
</ul>
<p>This composition by English monk and later saint, Bede, covers the history of Christianity in England up until the time of its composition in 731. It was referenced for the hagiography of St Alban.</p>
<ul>
<li>Blick, Sarah, and Tekippe, Rita, eds. <em>Art and Architecture of Late Medieval Pilgrimage in Northern Europe and the British Isles</em>. Leiden: Brill Academic Publishers, 2004.</li>
</ul>
<p>This book is a collection of works relating to material culture in the context of sites visited by pilgrims in the late medieval period. It informs discussions about the growing presence and economics of holy sites during this time period.</p>
<ul>
<li>Jeffs, Amy and Gabriel Byng. &quot;The Digital Pilgrim Project: 3D Modeling and GIS Mapping Medieval Badges at the British Museum.&quot; <em>Peregrinations: Journal of Medieval Art and Architecture</em> 6, no. 2 (2017): 80-90. <a href="https://digital.kenyon.edu/perejournal/vol6/iss2/10">https://digital.kenyon.edu/perejournal/vol6/iss2/10</a>.</li>
</ul>
<p>After the first stage of the Digital Pilgrims project was complete, this article was published discussing the then present results. This article provided description regarding the archival state of the badges which were digitized.</p>
<ul>
<li>Lead alloy ampulla, obverse: Thomas à Becket, nimbed and mitred with crozier, a knight at either side with drawn sword; reverse: pointed oval panel with the martyrdom, England, 1170-1200, 100 x 87 mm. London, The British Museum, 1921,0216.62. <a href="https://www.britishmuseum.org/collection/object/H_1921-0216-62">https://www.britishmuseum.org/collection/object/H_1921-0216-62</a>.</li>
