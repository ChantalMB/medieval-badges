</ul>
<p>This is the home page for the Digital Pilgrim Project. It provides an overview of the desired goals of the project, as well as the expected outcomes.</p>
<ul>
<li>Webb, Diana. <em>Pilgrims and Pilgrimage in the Medieval West</em>. London: I.B.Tauris &amp; Co, 2001.</li>
</ul>
<p>This book uses numerous primary sources to provide a detailed look at the motives behind pilgrimage in western medieval Europe, as well as the journeys and destinations associated with these acts of piety. The chapters &quot;Remembering Pilgrimage: Souvenirs&quot; and &quot;Indulgences and Jubilees&quot; offer particularly relevant insight into the use of objects such as badges and their association with notions of devotion and piety as they relate to pilgrimage.</p>

